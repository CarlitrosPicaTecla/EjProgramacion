package ej2optional;

import java.util.Optional;

public class Persona {

	private String nombre;
	private String mail;
	
	public Persona(String nombre, String mail) {
		super();
		this.nombre = nombre;
		this.mail = mail;
	}



	public String getNombre() {
		return nombre;
	}



	public String getMail() {
		return mail;
	}



	
	
}
